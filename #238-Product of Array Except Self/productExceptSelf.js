/**
 * @param {number[]} nums
 * @return {number[]}
 */
var productExceptSelf = function (nums) {
  const n = nums.length;
  const answer = new Array(n).fill(1);

  let prefix = 1;
  for (let i = 0; i < n; i++) {
    answer[i] = prefix === -0 ? Math.abs(prefix) : prefix;
    prefix *= nums[i];
  }

  let suffix = 1;
  for (let i = n - 1; i >= 0; i--) {
    answer[i] *= suffix === -0 ? Math.abs(suffix) : suffix;
    suffix *= nums[i];
  }

  return answer;
};

module.exports = productExceptSelf;
